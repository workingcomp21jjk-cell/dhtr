
import random

def гра():
    варіанти = ['камінь', 'ножиці', 'папір']
    
    while True:
        гравець_бал = 0
        комп_бал = 0
        print("\nПочинаємо серію ігор! Перший, хто набере 3 бали, перемагає.\n")
        
        while гравець_бал < 3 and комп_бал < 3:
            гравець = input("Введіть камінь, ножиці або папір: ").lower()
            if гравець not in варіанти:
                print("Невірний вибір. Спробуйте ще раз.")
                continue
            
            комп = random.choice(варіанти)
            print(f"Комп'ютер вибрав: {комп}")
            
            if гравець == комп:
                print("Нічия!\n")
            elif (гравець == 'камінь' and комп == 'ножиці') or \
                 (гравець == 'ножиці' and комп == 'папір') or \
                 (гравець == 'папір' and комп == 'камінь'):
                гравець_бал += 1
                print(f"Ви виграли цей раунд! Счёт: Ви {гравець_бал} - Комп'ютер {комп_бал}\n")
            else:
                комп_бал += 1
                print(f"Комп'ютер виграв цей раунд! Счёт: Ви {гравець_бал} - Комп'ютер {комп_бал}\n")
        
        # Визначення переможця серії
        if гравець_бал == 3:
            print("Вітаю! Ви перемогли серію!")
        else:
            print("На жаль, переміг комп'ютер.")
        
        # Запит про повторну гру
        ще_раз = input("Бажаєте зіграти ще раз? (так/ні): ").lower()
        if ще_раз != 'так':
            print("Дякуємо за гру! До зустрічі!")
            break

# Запуск гри
гра()